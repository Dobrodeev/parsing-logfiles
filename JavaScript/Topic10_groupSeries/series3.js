/**
 * Даны десять вещественных чисел. Найти их среднее арифметическое.
 */
let sum = 0;
let i = 1;

while (i < 6) {
    let num = +prompt('Введите число!', '');
    i++;

    if (num == 0) {
        alert("Помилка! Ви ввели число 0. Почніть спочатку")
    }
    sum += num;
}

alert(sum / --i);
console.log(sum);