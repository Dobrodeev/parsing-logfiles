/***
 Дано целое число N и набор из N целых чисел.
 Если в наборе имеются положительные числа, то вывести True; в противном случае вывести False.
 */
let n = +prompt(`Введіть кількість елементів`);
let i = 1;
while (i <= n) {
    num = +prompt("Введіть числа", );
    i++;
}
if (num > 0 ) {
    alert("True")
} else {
    alert("False")
}







